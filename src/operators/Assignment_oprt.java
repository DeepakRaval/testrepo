package operators;

public class Assignment_oprt {
	
	public static void main(String[]args){
		int a = 2;
		int var;
		var = a;
		System.out.println(a+"and"+var);
		var+=a;// var = var + a;
		System.out.println(var);
		var-=a;// var = var - a;
		System.out.println(var);
		var*=a;// var = var * a;
		System.out.println(var);
		var/=a;// var = var / a;
		System.out.println(var);
		var%=a;// var = var % a;
		System.out.println(var);
	}

}
